package Tipos;

/**
 *
 * @author diegocarvajal
 */
public class Persona {
    protected String nombre;
    protected int nit;
    protected int edad;

    public Persona(String nombre, int nit, int edad) {
        this.nombre = nombre;
        this.nit = nit;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNit() {
        return nit;
    }

    public void setNit(int nit) {
        this.nit = nit;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
